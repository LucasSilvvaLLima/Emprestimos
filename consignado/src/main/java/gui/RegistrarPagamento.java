    public void recebendo(String recebe) {
        cliente = clienteDAO.getPesquisarCliente(recebe);
        Double rendimentoTotal = clienteDAO.getRendimentoTotalCliente(cliente.getIdCliente());
        Double totalEmprestimo = cliente.getValorTotalEmprestimo();
        if (cliente.getRendimento() != null) {
            NumberFormat formatoMoeda = DecimalFormat.getCurrencyInstance(new Locale("pt", "BR"));
            Double rendimentoDivida = cliente.getRendimento();
            
            Double rendimentoPorcentagem = (cliente.getRendimento() * 100.0) / cliente.getValorTotalEmprestimo();
            String rendimentoPorcentagemString = String.format("%.2f", rendimentoPorcentagem);
            String rendimentoDividaFormatado = formatoMoeda.format(rendimentoDivida);
            campoRendimentoReais.setText(rendimentoDividaFormatado);
            campoRendimentoPorcentagem.setText(String.valueOf(rendimentoPorcentagemString));
        } else {
            campoRendimentoReais.setText("R$ 0,00");
            campoRendimentoPorcentagem.setText("0,00");
        }
        lerTabela(cliente);

        if (cliente.getValorTotalDivida() < 0) {
            cliente.setValorTotalDivida(0.0);
            cliente.setValorPagamentoMensal(0.0);
        }

        /*MASCARA DINHEIRO*/
        NumberFormat formatoMoeda = DecimalFormat.getCurrencyInstance(new Locale("pt", "BR"));
        String valorDividaFormatado = formatoMoeda.format(cliente.getValorTotalDivida());
        String valorPagamentoMensalFormatado = formatoMoeda.format(cliente.getValorPagamentoMensal());
        String rendimentoTotalFormatado = formatoMoeda.format(rendimentoTotal);
        String totalEmprestimoFormatado = formatoMoeda.format(totalEmprestimo);

        campoNome.setText(cliente.getNome());
        campoDataDivida.setText(cliente.getDataEmprestimo());
        campoValorAtualDivida.setText(valorDividaFormatado);
        campoValorPagamentoMensal.setText(valorPagamentoMensalFormatado);
        campoDataPagamentoMensal.setText(cliente.getDataPagamentoMensal());
        campoPorcentagem.setText(String.valueOf(cliente.getPorcentagem()));

        campoTotalEmprestimo.setText(String.valueOf(totalEmprestimoFormatado));
        campoRendimentoTotal.setText(rendimentoTotalFormatado);
    }

    public void validarPagamento(Double valorPago) {

        String padraoFormatacao = "dd/MM/yyyy";
        DateTimeFormatter formatador = DateTimeFormatter.ofPattern(padraoFormatacao);

        LocalDate dataPagamentoMensalLocalDate = LocalDate.parse(cliente.getDataPagamentoMensal(), formatador);
        LocalDate novaDataPagamentoMensalLocalDate = dataPagamentoMensalLocalDate.plus(Period.ofMonths(1));
        String novaDataPagamentoMensalString = novaDataPagamentoMensalLocalDate.format(formatador);

        cliente.setDataPagamentoMensal(novaDataPagamentoMensalString);
        cliente.setValorPagamentoMensal(valorPago);

        
        Boolean isPagamentoComValorExcedente = cliente.getValorPagamentoMensal() > (cliente.getValorTotalDivida() * (cliente.getPorcentagem() / 100.0));
        if (isPagamentoComValorExcedente) {
            cliente.setValorTotalDivida(cliente.getValorTotalDivida() - (valorPago - (cliente.getValorTotalDivida() * (Double.valueOf(cliente.getPorcentagem()) / 100))));
        }
        clienteDAO.registrarPagamento(cliente);
    }

    public void configurarTabela() {
        DefaultTableModel model = (DefaultTableModel) tableHistoricoPagamento.getModel();
        tableHistoricoPagamento.getTableHeader().setDefaultRenderer(new CenterRenderer());
        tableHistoricoPagamento.getColumnModel().getColumn(0).setCellRenderer(new CenterRenderer());
        tableHistoricoPagamento.getColumnModel().getColumn(1).setCellRenderer(new CenterRenderer());

        model.setNumRows(0);
    }

    public void lerTabela(Cliente historicoCliente) {
        configurarTabela();
        DefaultTableModel model = (DefaultTableModel) tableHistoricoPagamento.getModel();

        for (Cliente cliente : clienteDAO.getClientesHistorico(historicoCliente.getIdCliente())) {

            /*MASCARA DINHEIRO*/
            NumberFormat formatoMoeda = DecimalFormat.getCurrencyInstance(new Locale("pt", "BR"));
            String valorPagamentoMensalFormatado = formatoMoeda.format(cliente.getValorPagamentoMensal());

            model.addRow(new Object[]{
                valorPagamentoMensalFormatado,
                cliente.getDataPagamentoMensal()
            });
        }
    }

