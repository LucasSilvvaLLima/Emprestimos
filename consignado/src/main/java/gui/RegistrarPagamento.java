    public void registrarPagamento(){
        /*MASCARA DINHEIRO*/
        if (!campoValorPagoPeloCliente.getText().equals("")) {
            if (cliente.getValorTotalDivida() > 0) {
                Double confirmarValorPago = Double.valueOf(campoValorPagoPeloCliente.getText());
                NumberFormat formatoMoeda = DecimalFormat.getCurrencyInstance(new Locale("pt", "BR"));
                String confirmarValorPagoString = formatoMoeda.format(confirmarValorPago);
                String mensagem = "O VALOR PAGO DE " + confirmarValorPagoString + "\nESTA CORRETO ?";
                Integer confirmar = JOptionPane.showConfirmDialog(null, mensagem, "CONFIRME O VALOR!", JOptionPane.YES_NO_OPTION);
                if (confirmar == JOptionPane.YES_OPTION) {
                    Double valorPago = Double.valueOf(campoValorPagoPeloCliente.getText());
                    validarPagamento(valorPago);
                    fecharJanela();
                }
            } else {
                JOptionPane.showMessageDialog(null, "A divida desse cliente já foi finalizada", "ALERTA", 2);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Insira o valor pago no campo!", "ALERTA", 2);
        }
        campoValorPagoPeloCliente.setText("");
    }
